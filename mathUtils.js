
// ADDITION function
export function addTwoNumbers(firstNumber, secondNumber) {
    return firstNumber + secondNumber;
}

// SUBTRACTION function
export function subTwoNumbers(firstNumber, secondNumber) {
    return firstNumber - secondNumber;
}

// MULTIPLY function
export function mulTwoNumbers(firstNumber, secondNumber) {
    return firstNumber - secondNumber;
}

// DIVIDE function
export function divTwoNumbers(firstNumber, secondNumber) {
    return firstNumber - secondNumber;
}